#Ejemplos en otras sintasys 

#Los operadores aritméticos son utilizados en Python para realizar operaciones matemáticas en valores numéricos. 
# Aquí tienes los operadores aritméticos básicos en Python:

#Suma (+): Este operador se utiliza para sumar dos valores.
a = 5
b = 3
resultado = a + b
print(resultado)  # Salida: 8

#Resta (-): Este operador se utiliza para restaurar un valor de otro.
a = 7
b = 2
resultado = a - b
print(resultado)  # Salida: 5

#Multiplicación (*): Este operador se utiliza para multiplicar dos valores.
a = 4
b = 6
resultado = a * b
print(resultado)  # Salida: 24

# División (/): Este operador se utiliza para dividir un valor entre otro. El resultado siempre es un número de punto flotante.
a = 10
b = 3
resultado = a / b
print(resultado)  # Salida: 3.3333333333333335

#División entera (//): Este operador se utiliza para realizar una división entera entre dos valores. El resultado es el cociente de la división, sin decimales
a = 10
b = 3
resultado = a // b
print(resultado)  # Salida: 3

#Módulo (%): Este operador se utiliza para obtener el resto de la división entre dos valores.
a = 10
b = 3
resultado = a % b
print(resultado)  # Salida: 1

#Potenciación (**): Este operador se utiliza para aumentar un valor a una potencia.
a = 10
b = 3
resultado = a ** b
print(resultado)  # Salida: 8
#Estos son los operadores aritméticos básicos en Python. 
# Puede combinarlos y utilizar paréntesis para realizar operaciones más complejas y matemáticas.