#Operadores de asignación
#Los operadores de asignación nos permiten asignar valores a variables.

x = 10
x += 5 # x = x + 5
print(x) # 15

x -= 3 # x = x - 3
print(x) # 12

x *= 2 # x = x * 2
print(x) # 24

x /= 4 # x = x / 4
print(x) # 6.0

x //= 3 # x = x // 3
print(x) # 2.0

x %= 2 # x = x % 2
print(x) # 0.0

x **= 3 # x = x ** 3
print(x) # 0.0

#Ejemplos Reales 


#Asignación (=): El operador de básico se utiliza para asignar un valor a una variable.
x = 5
#signación con suma (+=): Este operador combina la operación de suma y estima. 
# Agrega el valor derecho al valor de la variable izquierda y asigna el resultado a la variable izquierda.

x = 5
x += 3  # Equivalente a: x = x + 3
print(x)  # Salida: 8
#Asignación con resta (-=): Este operador combina la operación de resta y redacta. 
# Resta el valor derecho al valor de la variable izquierda y asigna el resultado a la variable izquierda.

x = 10
x -= 4  # Equivalente a: x = x - 4
print(x)  # Salida: 6
#Asignación con multiplicación (*): Este operador combina la operación de multiplicación y reproducir. 
#Multiplica el valor derecho por el valor de la variable izquierda y asigna el resultado a la variable izquierda.

x = 3
x *= 2  # Equivalente a: x = x * 2
print(x)  # Salida: 6
#Asignación con división (/=): Este operador combina la operación de división y juzga. 
# Divide el valor de la variable izquierda por el valor derecho y asigna el resultado a la variable izquierda.
x = 10
x /= 2  # Equivalente a: x = x / 2
print(x)  # Salida: 5.0
