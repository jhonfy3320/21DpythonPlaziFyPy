'''Diccionarios:
Los diccionarios en Python son estructuras de datos que almacenan pares clave-valor. 
Puedes utilizarlos para representar atributos de un animal. 
Aquí tienes un ejemplo utilizando los atributos de un pájaro:'''

# Atributos de un pájaro
pajaro = {
    "especie": "canario",
    "color": "amarillo",
    "edad": 2,
    "ubicacion": "jardín"
}

'''En este ejemplo, el diccionario "pajaro" contiene información sobre la especie, el color, la edad y 
la ubicación de un pájaro en particular.'''